##>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>
sub LoadGoldStandards {

($infile) = @_;

print "Loading gold standards from:\n$infile\n";

open (INFILE, "<$infile") or die "Can't open '$infile' infile by LoadGoldStandards\n";

while ($line = <INFILE>) {
chomp $line;
	unless ($line =~ /^#/) {
		if ($line =~ /^(\S+)(\s+)(\S+)/) {
		$clusterid = $1;
		$classid   = $3;
		$hashGoldStandards{$clusterid} = $classid;
		}
	}
}
close INFILE;

}

