$ThisProgramName = $0;
$ThisProgramName =~ s/\S+\///;

$CommentsForHelp = "
#####################################################################################
################### START INSTRUCTIONS TO RUN THIS PROGRAM ##########################
###
### Entering a -infile_list_infiles of cell cluster labeling results from methods like CIBERSORT, GSEA, GSVA, ORA, METANEIGHBOR, etc.
